    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Chocolate chocolate;
        Registro registro=new Registro();
        try{
            chocolate=registro.Buscar(Integer.parseInt(jTextField1.getText().trim()));
            if(chocolate.getNombre()==null && chocolate.getCodigo()==0){javax.swing.JOptionPane.showMessageDialog(this, "No se encontró código");}
            else{
                System.out.println(chocolate.toString());
                jTextField2.setText(chocolate.getNombre());
                jTextField3.setText(chocolate.getDescripcion());
                jTextField4.setText(String.valueOf(chocolate.getPrecioVenta()));
                jTextField5.setText(String.valueOf(chocolate.getCantidad()));
                this.jComboBox1.setSelectedItem(chocolate.getTipo());
                this.jComboBox2.setSelectedItem(chocolate.getSabor());
                this.jComboBox3.setSelectedItem(chocolate.getMarca());
                if(chocolate.isAzucar()){jRadioButton1.setSelected(true);}
                else{jRadioButton2.setSelected(true);}
            }
        }
        catch(IllegalArgumentException g){javax.swing.JOptionPane.showMessageDialog(this,"Ingrese Código Numérico");}
    }//GEN-LAST:event_jButton1ActionPerformed

