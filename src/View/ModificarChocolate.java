    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Chocolate chocolate=new Chocolate();
        Registro registro=new Registro();
        Object cmbTipo=this.jComboBox1.getSelectedItem();
        Object cmbSabor=this.jComboBox2.getSelectedItem();
        Object cmbMarca=this.jComboBox3.getSelectedItem();
        try{
            chocolate.setCodigo(Integer.parseInt(jTextField1.getText().trim()));
            
            
            if(jTextField2.getText().trim().equals("")){throw new IllegalArgumentException();}
            if(jTextField2.getText().trim()!=""){
                chocolate.setNombre(jTextField2.getText().trim());}
            
            
            if(jTextField3.getText().trim().equals("")){throw new IllegalArgumentException();}
            if(jTextField3.getText().trim()!=""){chocolate.setDescripcion(jTextField3.getText().trim());}
            
            int pre=Integer.parseInt(jTextField4.getText().trim());
            if(pre>0){chocolate.setPrecioVenta(Integer.parseInt(jTextField4.getText().trim()));}
            else{javax.swing.JOptionPane.showMessageDialog(this, "Precio debe ser mayor que 0");throw new IllegalArgumentException();}
            
            
            int cant=Integer.parseInt(jTextField5.getText().trim());
            if(cant>=0){chocolate.setCantidad(Integer.parseInt(jTextField5.getText().trim()));}
            else{javax.swing.JOptionPane.showMessageDialog(this, "Cantidad debe ser mayor o igual que 0");throw new IllegalArgumentException();}
            
            
            if(cmbTipo.toString().equals("Seleccionar")){throw new IllegalArgumentException();}
            else{chocolate.setTipo(cmbTipo.toString());}
            if(cmbSabor.toString().equals("Seleccionar")){throw new IllegalArgumentException();}else{chocolate.setSabor(cmbSabor.toString());}
            if(cmbMarca.toString().equals("Seleccionar")){throw new IllegalArgumentException();}else{chocolate.setMarca(cmbMarca.toString());}
            
            
            if(jRadioButton1.isSelected()){chocolate.setAzucar(true);}
            if(jRadioButton2.isSelected()){chocolate.setAzucar(false);}
            if(jRadioButton1.isSelected()==false && jRadioButton2.isSelected()==false){throw new IllegalArgumentException();}
            if(registro.Modificar(chocolate)){javax.swing.JOptionPane.showMessageDialog(this, "Chocolate Modificado");}
            else{javax.swing.JOptionPane.showMessageDialog(this, "Chocolate No Modificado");}
        }
        catch(IllegalArgumentException g){javax.swing.JOptionPane.showMessageDialog(this, "Valores Inválidos o No Ingresados");}
        
    }//GEN-LAST:event_jButton4ActionPerformed

